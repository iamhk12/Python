#FOR loop

# in python : 
#   for loop is used to iterate items in a sequence like list, tuple, string (literals)


'''
SYNTAX:

for item in list:
    //statements//
    e.g: print(item)


'''


# Print a list using for loop

fruits = ["apple", "banana", "grapes", "mango"]

for item in fruits:
    print(item)

#Print from 1 to 8 using for loop
for i in range(1,9):
    print(i) #prints 1 to 8

    









































