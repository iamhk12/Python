# LISTS in PYTHON

##################################################################################

a = [1, 2, 3, 4, 5, 6, 7, 101]
print(a)

##################################################################################

# indexing starts from 0
print(a[0])  # o/p => 1
print(a[4])  # o/p => 5

#print(a[13])  #error...IndexError: list index out of range

##################################################################################

#Lists item value can be changed
a[0] = 98
print(a) # [98, 2, 3, 4, 5, 6, 7, 101]


